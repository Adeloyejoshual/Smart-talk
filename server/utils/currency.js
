import axios from "axios";

export async function detectCurrency(ip) {
  try {
    // Free IP geolocation API
    const res = await axios.get(`https://ipapi.co/${ip}/json/`);
    return res.data.currency || "USD";
  } catch (err) {
    console.error("Currency detect failed:", err.message);
    return "USD";
  }
}

export async function convertToUSD(amount, fromCurrency) {
  try {
    if (fromCurrency === "USD") return amount;

    const res = await axios.get(
      `https://api.exchangerate.host/convert?from=${fromCurrency}&to=USD&amount=${amount}`
    );

    return res.data.result || amount;
  } catch (err) {
    console.error("Conversion failed:", err.message);
    return amount;
  }
}